    private void cdastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cdastrarActionPerformed
    if(mesa.getText().equals("")){
    JOptionPane.showMessageDialog(null, "Preencha o campo Mesa!");
    }if(quantidade.getText().equals("")){
    JOptionPane.showMessageDialog(null, "Preencha o campo Quantidade!");
    
    }else{    
        campos_Pedido cp = new campos_Pedido();
        cp.setMesa(Integer.parseInt(mesa.getText()));
        cp.setPedido(pedido.getText());
        cp.setQuantidade(Integer.parseInt(quantidade.getText()));
        cp.setValorTotal(Double.parseDouble(valorTotal.getText()));
        cp.setValor(Double.parseDouble(valor.getText()));
        cp.setTfcodigopedido(Integer.parseInt(idpedido.getText()));
        
        
        FuncaoPedido cpe = new FuncaoPedido();
        cpe.CadastraPedido(cp);
      
        limpar();
        listarPedidos();
    }
    }//GEN-LAST:event_cdastrarActionPerformed

