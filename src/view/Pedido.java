      public void pesquisarPedido(){
    
     String sql = "Select mesa,pedido,quantidade,preco,status,nome from listarpedido where mesa like ? order by mesa desc";
     try{
     pst = con.prepareStatement(sql);
     pst.setString(1, pesquisar.getText()+"%");
     rs = pst.executeQuery();
     tabela.setModel(DbUtils.resultSetToTableModel(rs));
     
     }
     catch(SQLException error){
     JOptionPane.showMessageDialog(null,error);
     
     }
     
     }
   
    public void listarPedidos(){
    String sql = "select mesa,pedido,quantidade,preco,status,nome from listarpedido order by mesa desc";
    
    try{
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        tabela.setModel(DbUtils.resultSetToTableModel(rs));
              
    }
    catch(SQLException error){
    
    JOptionPane.showMessageDialog(null,error);
    
    }
    }
    
     public void mostraItens(){
     int seleciona = tabela.getSelectedRow();
     mesa.setText(tabela.getModel().getValueAt(seleciona,0).toString());
     pedido.setText(tabela.getModel().getValueAt(seleciona,1).toString());
     quantidade.setText(tabela.getModel().getValueAt(seleciona,2).toString());
     valor.setText(tabela.getModel().getValueAt(seleciona, 3).toString());
     String sql = "Select Id,CodPedido,total from listarpedido where mesa like ?";
     try{
     pst = con.prepareStatement(sql);
     pst.setString(1, mesa.getText());
     rs = pst.executeQuery();
     if(rs.next()){
         codigo.setText(rs.getString("Id"));
         idpedido.setText(rs.getString("CodPedido"));
         valorTotal.setText(rs.getString("total"));
     }}catch(SQLException erro){    
         JOptionPane.showMessageDialog(rootPane, erro);
    }}
          
