       public void listarFuncionario(){
    String sql = "select * from listarfuncionario order by nome asc";
    
    try{
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        tabela1.setModel(DbUtils.resultSetToTableModel(rs));
              
    }
    catch(SQLException error){
    
    JOptionPane.showMessageDialog(null,error);
    
    }
   
    }
     
    public void pesquisarFuncionario(){
    
     String sql = "Select * from listarfuncionario where nome like ? order by nome  asc";
     try{
     pst = con.prepareStatement(sql);
     pst.setString(1, pesquisar.getText()+"%");
     rs = pst.executeQuery();
     tabela1.setModel(DbUtils.resultSetToTableModel(rs));
     
     }
     catch(SQLException error){
     JOptionPane.showMessageDialog(null,error);
     
     }
     
     }
