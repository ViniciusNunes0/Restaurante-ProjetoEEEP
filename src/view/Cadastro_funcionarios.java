    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
    if(nome.getText().equals("")|| bairro.getText().equals("") || cep.getText().equals("") || cpf.getText().equals("")
            || dataNasc.getText().equals("") || rg.getText().equals("") || email.getText().equals("") 
            || telefone.getText().equals("") 
            || rua.getText().equals("")){
    JOptionPane.showMessageDialog(null, "Preencha todos os Campos!");
   
    }else{    
        
        Cadastro_Funcionario cp = new Cadastro_Funcionario();
        cp.setNome(nome.getText());
        cp.setBairro(bairro.getText());
        cp.setCEP(cep.getText());
        cp.setCPF(cpf.getText());
        cp.setData_nascimento(dataNasc.getText());
        cp.setRG(rg.getText());
        cp.setEmail(email.getText());
        cp.setTelefone(telefone.getText());
        cp.setSexo((String) sexo.getSelectedItem());
        cp.setRua(rua.getText());
        
        CadastrarFuncionario cf = new CadastrarFuncionario();
        cf.CadastrarFuncionario(cp);
        
        
   listarFuncionario();
  
    }
    }//GEN-LAST:event_salvarActionPerformed

