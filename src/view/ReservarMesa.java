    public void listar(){
    String sql = "select mesa,status,nome from listarpedido order by mesa desc";
    
    try{
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        tabela.setModel(DbUtils.resultSetToTableModel(rs));
              
    }
    catch(SQLException error){
    
    JOptionPane.showMessageDialog(null,error);
    
    }
    }
    public void pesquisarNome(){
    
     String sql = "Select mesa,status,nome from listarpedido where mesa like ? order by mesa desc";
     try{
     pst = con.prepareStatement(sql);
     pst.setString(1, pesquisar.getText()+"%");
     rs = pst.executeQuery();
     tabela.setModel(DbUtils.resultSetToTableModel(rs));
     
     }
     catch(SQLException error){
     JOptionPane.showMessageDialog(null,error);
     
     }
     
     }
    public void limpar(){
        mesa.setText("");
        status.setSelectedIndex(0);
        nome.setText("");
    }
     public void itens(){
     int s=tabela.getSelectedRow();
     mesa.setText(tabela.getModel().getValueAt(s, 0).toString());
     status.setSelectedItem(tabela.getModel().getValueAt(s, 1).toString());
     nome.setText(tabela.getModel().getValueAt(s, 2).toString().toString());
     }
    
    
