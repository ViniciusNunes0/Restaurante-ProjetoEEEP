    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
  ResevarControle rc = new ResevarControle();
  List<ResevarModel> lista = new ArrayList<>();
  lista = rc.verificarResevarmesa( mesa.getText(), status.getSelectedItem().toString(), nome.getText());
  if(lista.size()>0){
      JOptionPane.showMessageDialog(rootPane, "Ja está Reservado "+nome.getText());
  }else{    
        
    ResevarModel rm = new ResevarModel();
    rm.setMesa(Integer.parseInt(mesa.getText()));
    rm.setNome(nome.getText());
    rm.setStatus(status.getSelectedItem().toString());
    
    ResevarControle rr = new ResevarControle();
    rr.salvar(rm);
    limpar(); 
    listar();
  }
    }//GEN-LAST:event_jButton1ActionPerformed

