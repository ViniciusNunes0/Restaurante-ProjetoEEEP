    private void salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_salvarActionPerformed
        // campo p/ codigo do bultao salvar / comandos para salvar:
        Cadastro_Alimento cp = new Cadastro_Alimento();

        cp.setNome(alimento.getText());
        cp.setpreco(Double.parseDouble(preco.getText()));

        Cadastrar_Alimento cf = new Cadastrar_Alimento();
        cf.CadastrarAlimento(cp);
        listar_alimento();
        limpar();
    }//GEN-LAST:event_salvarActionPerformed

    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed
        // TODO add your handling code here:
        Cadastro_Alimento cp = new Cadastro_Alimento();
        cp.setcodigo(Integer.parseInt(id.getText()));
        cp.setNome(alimento.getText());
        cp.setpreco(Double.parseDouble(preco.getText()));

        Cadastrar_Alimento cf = new Cadastrar_Alimento();
        cf.editar(cp);

        listar_alimento();
        limpar();
    }//GEN-LAST:event_alterarActionPerformed

