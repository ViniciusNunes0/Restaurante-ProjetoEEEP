 public void listar_alimento(){
    String sql = "select * from alimentos";
    
    try{
        pst = con.prepareStatement(sql);
        rs = pst.executeQuery();
        tabela.setModel(DbUtils.resultSetToTableModel(rs));
              
    }
    catch(SQLException error){
    
    JOptionPane.showMessageDialog(null,error);
    
    }
 
    }
       public void pesquisar_Alimento(){
    
     String sql = "select * from alimentos where nome like ?";
     try{
     pst = con.prepareStatement(sql);
     pst.setString(1, pesquisar.getText()+"%");
     rs = pst.executeQuery();
     tabela.setModel(DbUtils.resultSetToTableModel(rs));
     
     }
     catch(SQLException error){
     JOptionPane.showMessageDialog(null,error);
     
     }
     
     }
        public void mostraItens(){
     
         int seleciona = tabela.getSelectedRow();
         id.setText(tabela.getModel().getValueAt(seleciona,0).toString());
         alimento.setText((tabela.getModel().getValueAt(seleciona,1).toString()));
         preco.setText(tabela.getModel().getValueAt(seleciona,2).toString());
          }
        
        
        public void limpar(){
        pesquisar.setText("");
        id.setText("");
        alimento.setText("");
        preco.setText("");
        pesquisar.requestFocus();
        
        }
